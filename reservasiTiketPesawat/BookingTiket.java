    public void autoID() {
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/maskapai", "root", "");
        Statement s = con.createStatement();
        ResultSet rs = s.executeQuery("SELECT MAX(tiket) AS max_tiket FROM pesentiket");
        
        if (rs.next()) {
            String maxTiket = rs.getString("max_tiket");
            if (maxTiket == null) {
                txtautotick.setText("001");
            } else {
                long id = Long.parseLong(maxTiket.substring(2));
                id++;
                txtautotick.setText(String.format("%03d", id));
            }
        }
        
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
    }
}
      
    

