    private void penumStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_penumStateChanged
        // TODO add your handling code here:
        try {
            // Mendapatkan harga dari JTextField dan membersihkannya dari karakter yang tidak diperlukan
            String hargaText = txtharga.getText().replaceAll("[^\\d]", "");
            int harga = Integer.parseInt(hargaText);
            
            // Mendapatkan jumlah kursi dari JSpinner
            int kursi = (Integer) penum.getValue();
            
            // Menghitung total harga
            int total = harga * kursi;

            // Memformat angka menjadi nominal Rupiah
            NumberFormat formatRupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
            String totalFormatted = formatRupiah.format(total);

            // Menampilkan format rupiah
            toga.setText(totalFormatted);
        } catch (NumberFormatException e) {
    
        }
    
    }//GEN-LAST:event_penumStateChanged

